                        <p align="justify">The United Nations adopted the Universal Declaration of Human Rights on 10 December 1948, which proclaims that all human beings are born free and have equal rights to dignity. Similarly, the Indian Constitution guarantees several rights, such as the right to equality in Article 14 and the right to life and personal liberty under Article 21 of the Constitution, to all its citizens irrespective of gender.</p>
                        <p align="justify">Gender equality means a society wherein both women and men enjoy the same opportunities, rights, and obligations in different spheres of life. Equality in decision-making, economic and social freedom, equal access to education, and the right to practice an occupation of one’s choice. To promote gender equality, we need to empower women and concentrate on areas that are most crucial to their well-being. Women’s empowerment, economic, social, and political, is vital to the growth of any nation and to protecting and nurturing human rights.</p>
