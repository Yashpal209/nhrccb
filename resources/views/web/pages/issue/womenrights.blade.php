                <h4>Constitutional rights available to women in India-</h4>
                <ul class="mt-3">
                    <li>
                        <p align="justify">The fundamental right to equality before the law, that is, equal protection of laws in India- Article 14</p>
                    </li>
                    <li>
                        <p align="justify">Prohibition of discrimination on grounds of religion, race, caste, sex, or place of birth. However, Article 15(3) empowers the state to make any special provision for women and children—Article 15</p>
                    </li>
                    <li>
                        <p align="justify">Equality of opportunity in matters of public employment or opportunity to any office under state and prohibits discrimination on grounds of sex—Article 16</p>
                    </li>
                    <li>
                        <p align="justify">Freedom of speech and expression and freedom to practice any profession or to carry out any occupation, trade or business— Article 19</p>
                    </li>
                    <li>
                        <p align="justify">Protection of life and personal liberty- Article 21</p>
                    </li>
                    <li>
                        <p align="justify">Right to Privacy- Article 21</p>
                    </li>
                    <li>
                        <p align="justify">Right to property- Article 300-A</p>
                    </li>
                    <li>
                        <p align="justify">Political rights- Women reservation in for instance, panchayats. Art 15 of the Constitution allows special provisions for women and children to be made for their welfare.</p>
                    </li>
                    <li>
                        <p align="justify">Under the Legal Services Authorities Act, women and children are entitled to free legal aid.</p>
                    </li>
