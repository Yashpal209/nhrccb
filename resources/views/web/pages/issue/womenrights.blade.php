                <p align="justify">Under the Constitution of India, the Directive Principles of State Policy contain duties of the state to apply these principles while making laws. These principles state that the state shall direct its policies to ensure that citizens, men, and women equally have the right to an adequate means of livelihood; that there is equal pay for both men and women, and that there is free and compulsory education for children and a duty to improve public health. Whereas in the case of violation of fundamental rights, these rights are enforceable; that is, a victim can seek legal redress through a court of law; the directive principles are only a guiding factor, and its nonobservance is not actionable before a court of law.</p>
