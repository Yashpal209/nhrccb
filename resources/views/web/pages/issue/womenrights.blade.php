            <div class="mt-3">
                <h3>10 Legal Rights Every Woman Must Know</h3>
                <p align="justify">Knowing the times that we are in, we give you, yet again, an overview of some important legal rights for women that everyone must be aware of.</p>

                <h4>Free legal aid</h4>
                <p align="justify">Exercise your right to free legal aid. Often, women go to the police station unaccompanied by a lawyer to get their statements recorded, and they stand the risk of being misquoted or their statement being tampered with. The police may also treat the entire episode lightly and not lodge an FIR. So, it is necessary to have a lawyer with you while you lodge the FIR.</p>
                <em>“According to a Delhi High Court ruling, whenever a rape is reported, the SHO has to bring this to the notice of the Delhi Legal Services Authority. The legal body then arranges for a lawyer for the victim.”</em>
                <p align="right">-	says Saumya Bhaumik, a women's rights lawyer.</p>

                <h4>Right to privacy while recording statement</h4>
                <p align="justify">Under section 164 of the Criminal Procedure Code, a woman who has been raped can record her statement before the district magistrate when the case is under trial, and no one else needs to be present. Alternatively, she can record the statement with only one police officer and a woman constable in a convenient place that is not crowded and does not provide any possibility of the statement being overheard by a fourth person. The cops have to, by law, uphold the woman’s right to privacy. It’s important for the person to feel comfortable and not be under any kind of stress while narrating the incident.</p>

                <h4>Time doesn’t matter</h4>
                <p align="justify">The police cannot refuse to register an FIR even if a considerable period has elapsed since the incident of rape or molestation took place. If the police tell you that they can’t lodge your FIR since you didn’t report it earlier, do not concede.</p>
                <em>“Rape is a horrifying incident for any woman, so it’s natural for her to go into shock and not want to report it immediately. She may also fear for her safety and the reputation and dignity of her family. For this reason, the Supreme Court has ruled that the police must register an FIR even if there has been a gap between the report and the occurrence of the incident.” </em>
                <p align="right">-	says Tariq Abeed, advocate, Supreme Court.</p>

                <h4>Email to the rescue</h4>
                <p align="justify">According to the guidelines issued by the Delhi Police, a woman has the privilege of complaining about email or registered post. If, for some reason, a woman can’t go to the police station, she can send a written complaint through an email or registered post addressed to a senior police officer of the level of Deputy Commissioner or Commissioner of Police. The officer then directs the SHO of the police station of the area where the incident occurred to conduct proper verification of the complainant and lodge an FIR. The police can then come over to the residence of the victim to take her statement.</p>

                <h4>Cops can’t say no</h4>
                <p align="justify">A rape victim can register her police complaint from any police station under the Zero FIR ruling by the Supreme Court. </p>
                <em>“Sometimes, the police station under which the incident occurs refuses to register the victim’s complaint in order to keep clear of responsibility and tries sending the victim to another police station. In such cases, she has the right to lodge an FIR at any police station in the city under the Zero FIR ruling. The senior officer will then direct the SHO of the concerned police station to lodge the FIR”</em>
                <p align="right">-	says Abeed. </p>
                <p align="justify">This is a Supreme Court ruling that not many women are aware of, so don’t let the SHO of a police station send you away saying it “doesn’t come under his area.”</p>
                
                <h4>No arrests after sunset</h4>
                <p align="justify">According to a Supreme Court ruling, a woman cannot be arrested after sunset and before sunrise. There are many cases of women being harassed by the police during the wee hours, but all this can be avoided if you exercise the right to be present in the police station only during the daytime. </p>
                <em>“Even if a woman constable is accompanying the officers, the police can’t arrest a woman at night. In case the woman has committed a serious crime, the police have to get it in writing from the magistrate explaining why the arrest is necessary during the night.” </em>
                <p align="right">-	says Bhaumik</p>
                    
                <h4>You can’t be called to the police station</h4>
                <p align="justify">Women cannot be called to the police station for interrogation under Section 160 of the Criminal Procedure Code. This law provides Indian women the right to not be physically present at the police station for interrogation. </p>
                <em>“The police can interrogate a woman at her residence in the presence of a woman constable and family members or friends.” </em>
                <p align="right">-	says Abeed. </p>
                <p align="justify">So, the next time you’re called to the police station for queries or interrogation when you have faced any kind of harassment, quote this guideline of the Supreme Court to exercise your right and remind the cops about it.</p>


                <h4>Protect your identity</h4>
                <p align="justify">Under no circumstances can the identity of a rape victim be revealed. Neither the police nor the media can make known the name of the victim in public. Section 228-A of the Indian Penal Code makes the disclosure of a victim’s identity a punishable offense. Printing or publishing the name or any matter that may make known the identity of a woman against whom an offense has been committed is punishable. This is done to prevent social victimization or ostracism of the victim of a sexual offense. Even while a judgment is in progress at the high court or a lower court, the name of the victim is not indicated, she is only described as a ‘victim’ in the judgment.</p>

                <h4>The doctor can’t decide</h4>
                <p align="justify">A case of rape can’t be dismissed even if the doctor says rape had not taken place. A victim of rape needs to be medically examined as per Section 164A of the Criminal Procedure Code, and only the report can act as proof. </p>
                <em>“A woman has the right to have a copy of the medical report from the doctor. Rape is a crime and not a medical condition. It is a legal term and not a diagnosis to be made by the medical officer treating the victim. The only statement that can be made by the medical officer is that there is evidence of recent sexual activity. Whether the rape has occurred or not is a legal conclusion, and the doctor can’t decide on this.” </em>
                <p align="right">-	explains Bhaumik.</p>

                <h4>Employers must protect</h4>
                <p align="justify">It is the duty of every employer to create a Sexual Harassment Complaints Committee within the organization for the redressal of such complaints. According to a guideline issued by the Supreme Court, it is mandatory for all firms, public and private, to set up these committees to resolve matters of sexual harassment. It is also necessary that the committee be headed by a woman and comprise 50% women as members. Also, one of the members should be from a women’s welfare group.</p>

