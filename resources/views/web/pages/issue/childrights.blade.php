            <h3 class="text-center mt-4">IMPACT OF THE CONVENTION OF THE CHILD RIGHTS</h3>
            <p align="justify">A milestone in international human rights legislation, the Convention on the Rights of
                the Child has been instrumental in putting all the issues about children's issues on the global as well
                as national agenda. In addition to this, it has extensively mobilized actions for the realization of the
                rights and development of children worldwide.</p>
            <p align="justify">It was not an overnight initiative that resulted in the adoption of child rights. It took
                several years of movements and activism to shape favorable, positive, and constructive attitudes toward
                children and also incite actions to improve their well-being. The enormous efforts involved in the
                implementation of the Convention, the significant amount of resources committed to this cause, and the
                overall effectiveness of the systems put in place for the execution process have a bearing on the
                success of child well-being outcomes.</p>
            <p align="justify">Over the last 20 or so years, implementation of the Convention and its effect on child
                well-being varied from country to country and from one region of the world to the other. Based on
                analysis, there has been outstanding progress at a global level in addressing the issues related to
                children. These include progress in access to services, reaching their fullest potential through
                education, enactment of laws that uphold the principle of the best interests of the child, and child
                survival.</p>
            <p align="justify">Though noteworthy progress has been achieved, in developing countries, particularly
                India, there is still a long way to go in realizing the rights of children. Though all the relevant
                rules and policies are in place, there is a lack of enforcement initiatives. As barriers, several
                factors forbid the effective implementation of the laws. Due to relatively low success in achieving
                concrete child development outcomes in India, the condition of underprivileged kids and underprivileged
                youth is harsh and needs urgent attention. There is a need to intensify efforts for children's welfare
                at all levels to implement the rules and provisions of the Convention and contribute to creating a world
                suitable for children.</p>

