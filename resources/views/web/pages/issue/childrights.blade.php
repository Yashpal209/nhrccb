                history—Ms. Malala Yousafzai, these famous children’s rights activist have put commendable efforts into
                helping improve the lives of the youngest citizens.</p>
            <p align="justify">2014 Nobel Peace Prize awardees Ms. Malala Yousafzai and Mr. Kailash Satyarthi have
                reminded us all of the need to keep on advancing in providing opportunities that has an important effect
                on all children. The opportunities are meant to be meaningful enough to allow them to learn and gain the
                mindsets and skills that would empower them to be free and develop themselves, their communities and the
                world.</p>
            <p align="justify">Mr. Kailash Satyarthi’s struggle to liberate children from child labor had cost him many
                life threats, including bullet wounds by those who exploit young boys and girls for economic gain.
                Wearing flak jackets and armed with strong determination, he and his team raided many illegal factories
                and mines to rescue the children who were sold into servitude. It has been 30 years now since he started
                his movement. A movement that has one clear purpose—no child shall be a slave.</p>
            <p align="justify">On the other hand, when one thinks of Ms. Malala Yousafzai, the first thing that pops
                into one’s mind is education. The second is education for girls. In 2009, when she was just 11, she
