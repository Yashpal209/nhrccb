            <p align="justify">People from across the world striving for social justice have often directed their efforts toward the most vulnerable in society—the children. From Princess Diana’s charitable work on behalf of children to the efforts of activists like Grace Abbott and the youngest Nobel laureate in history—Ms. Malala Yousafzai, these famous children’s right activists have put commendable efforts in helping improve the lives of the youngest citizens.</p>
            <p align="justify">2014 Nobel Peace Prize awardees—Ms. Malala Yousafzai and Mr. Kailash Satyarthi have reminded us all of the need to keep on advancing in providing opportunities that has an important effect on all children. The opportunities are meant to be meaningful enough to allow them to learn and gain the mindsets and skills that would empower them to be free, develop themselves, their communities and the world.</p>
