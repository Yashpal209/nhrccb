                        <h3 class="text-center">Right to be Informed</h3>
                        <p align="justify">This means the right to be informed about the quality, quantity, potency,
                            purity, standard, and price of goods to protect the consumer against unfair trade practices.
                            Consumers should insist on getting all the information about the product or service before
                            making a choice or a decision. This will enable him to act wisely and responsibly and also
                            enable him to desist from falling prey to high-pressure selling techniques.</p>

                        <h3 class="text-center">Right to Choose</h3>
                        <p align="justify">It means the right to be assured, wherever possible, of access to a variety
                            of goods and services at competitive prices. In the case of monopolies, it means the right
                            to be assured of satisfactory quality and service at a fair price. It also includes the
                            right to basic goods and services. This is because the unrestricted right of the minority to
                            choose can mean a denial for the majority of its fair share. This right can be better
                            exercised in a competitive market where a variety of goods are available at competitive
                            prices.</p>

                        <h3 class="text-center">Right to be Heard</h3>
                        <p align="justify">It implies that the interests of consumers will be fairly taken into account
                            in the proper forums. It also includes the right to be represented in various forums formed
                            to consider the consumerâ€™s welfare. The consumers should form non-political and
                            non-commercial consumer organizations that can be given representation in various committees
                            formed by the government and other bodies in matters relating to consumers. </p>

                        <h3 class="text-center">Right to Seek Redressal</h3>
                        <p align="justify">This means the right to seek redressal against unfair trade practices or
                            unscrupulous exploitation of consumers. It also includes the right to a fair settlement of
                            the genuine grievances of the consumer. Consumers must make complaints for their genuine
                            grievances. Many times their complaint may be of small value but its impact on the society
                            as a whole may be very large. They can also take the help of consumer organizations in
                            seeking redressal of their grievances.</p>

                        <h3 class="text-center">Right to Cosumer Education</h3>
                        <p align="justify">This means the right to acquire the knowledge and skill to be an informed
                            consumer throughout life. Ignorance of consumers, particularly of rural consumers, is mainly
                            responsible for their exploitation. They should know their rights and must exercise them.
                            Only then can real consumer protection be achieved with success.</p>

