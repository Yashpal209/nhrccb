                    <div class="mt-2">
                        <h3 class="text-center">Right to Safety</h3>
                        <p align="justify">Means right to be protected against the marketing of goods and services, which are hazardous to life and property. The purchased goods and services availed of should not only meet their immediate needs, but also fulfill long term interests. Before purchasing, consumers should insist on the quality of the products as well as on the guarantee of the products and services. They should preferably purchase quality marked products such as ISI, AGMARK, etc</p>
                        <h3 class="text-center">Right to be Informed</h3>
                        <p align="justify">Means right to be informed about the quality, quantity, potency, purity, standard and price of goods so as to protect the consumer against unfair trade practices. Consumer should insist on getting all the information about the product or service before making a choice or a decision. This will enable him to act wisely and responsibly and also enable him to desist from falling prey to high pressure selling techniques.

</p>
                        <h3 class="text-center">Right to Choose
                        </h3>
                        <p align="justify">Means right to be assured, wherever possible of access to variety of goods and services at competitive price. In case of monopolies, it means right to be assured of satisfactory quality and service at a fair price. It also includes right to basic goods and services. This is because unrestricted right of the minority to choose can mean a denial for the majority of its fair share. This right can be better exercised in a competitive market where a variety of goods are available at competitive prices

</p>
                        <h3 class="text-center">Right to be Heard
                        </h3>
                        <p align="justify">Means that consumer’s interests will receive due consideration at appropriate forums. It also includes right to be represented in various forums formed to consider the consumer’s welfare. The Consumers should form non-political and non-commercial consumer organizations which can be given representation in various committees formed by the Government and other bodies in matters relating to consumers</p>
                        <h3 class="text-center">Right to Seek Redressal</h3>
                        <p align="justify">Means right to seek redressal against unfair trade practices or unscrupulous exploitation of consumers. It also includes right to fair settlement of the genuine grievances of the consumer. Consumers must make complaint for their genuine grievances.Many a times their complaint may be of small value but its impact on the society as a whole may be very large. They can also take the help of consumer organisations in seeking redressal of their grievances.

</p>
                        <h3 class="text-center">Right to Cosumer Education
                        </h3>
                        <p align="justify">Means the right to acquire the knowledge and skill to be an informed consumer throughout life.Ignorance of consumers, particularly of rural consumers, is mainly responsible for their exploitation. They should know their rights and must exercise them. Only then real consumer protection can be achieved with success.

</p>
                      

