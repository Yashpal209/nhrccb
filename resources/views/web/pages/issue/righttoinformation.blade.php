                        <h3 class="text-center">Summary of RTI Act 2005</h3>
                        <li>
                            <p align="justify">An Act to provide for setting out the practical regime of the right to
                                information for citizens to secure access to information under the control of public
                                authorities to promote transparency and accountability in the working of every public
                                authority, the constitution of a Central Information Commission and State Information
                                Commissions and for matters connected therewith or incidental thereto.</p>
                        </li>
                        <li>
                            <p align="justify">WHEREAS the Constitution of India has established a democratic Republic;
                            </p>
                        </li>
                        <li>
                            <p align="justify">AND WHEREAS democracy requires an informed citizenry and transparency of
                                information, which are vital to its functioning and also to contain corruption and to
                                hold Governments and their instrumentalities accountable to the governed;</p>
                        </li>
                        <li>
                            <p align="justify">AND WHEREAS revelation of information in actual practice is likely to
                                conflict with other public interests, including efficient operations of the Governments,
                                optimum use of limited fiscal resources, and the preservation of confidentiality of
                                sensitive information;</p>
                        </li>
                        <li>
                            <p align="justify">AND WHEREAS it is necessary to harmonize these conflicting interests
                                while preserving the paramount of the democratic ideal;</p>
                        </li>
                        <li>
                            <p align="justify">NOW, THEREFORE, it is expedient to provide for furnishing certain
                                information to citizens who desire to have it.</p>
